// Constants refer to fixed values that the program may not alter and they are called literals.
// Constants can be of any of the basic data types and can be divided into Integer Numerals, Floating-Point Numerals, Characters, Strings and Boolean Values.
// Constants are like a variable{Variables.cpp}, except that their value never changes during execution once defined.
// There are two other different ways to define constants in C++. These are:
// By using const keyword & By using #define preprocessor.
// Syntax to declare a constant:
// const [data_type] [constant_name]=[value];